    private void IdBarang() {
        java.sql.Connection conn = new Koneksi().connect();
        try {
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("select *from tb_barang ORDER BY idbarang DESC");
            if (rs.first() == false) {
                txtIdBarang.setText("B0001");
            } else {
                rs.first();
                System.out.println("COT: " + rs.getString("idbarang").substring(1, 5));
                int nokirim = Integer.valueOf(rs.getString("idbarang").substring(1, 5)) + 1;
                System.out.println(nokirim);
                if (nokirim < 10) {
                    txtIdBarang.setText("B000" + nokirim);
                } else if (nokirim >= 10 && nokirim < 100) {
                    txtIdBarang.setText("B00" + nokirim);
                } else if (nokirim >= 100 && nokirim < 1000) {
                    txtIdBarang.setText("B0" + nokirim);
                } else if (nokirim >= 1000 && nokirim < 9999) {
                    txtIdBarang.setText("B" + nokirim);
                }
            }
            rs.close();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    }

    private void ShowKategori() {
        java.sql.Connection conn = new Koneksi().connect();
        try {
            java.sql.Statement stmt = conn.createStatement();
            java.sql.ResultSet res = stmt.executeQuery("select *from tb_kategori");
            while (res.next()) {
                cbKategori.addItem(res.getString("kategori"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Kategori Gagal di Tampilkan !!");
        }
    }
    
